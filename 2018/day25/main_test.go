package main

import (
	"testing"
)

func TestHotChocolatePortal(t *testing.T) {
	tests := []struct {
		in  []string
		out int
	}{
		{[]string{
			"0,0,0,0",
			"3,0,0,0",
			"0,3,0,0",
			"0,0,3,0",
			"0,0,0,3",
			"0,0,0,6",
			"9,0,0,0",
			"12,0,0,0",
		}, 2},
		{[]string{
			"-1,2,2,0",
			"0,0,2,-2",
			"0,0,0,-2",
			"-1,2,0,0",
			"-2,-2,-2,2",
			"3,0,2,-1",
			"-1,3,2,2",
			"-1,0,-1,0",
			"0,2,1,-2",
			"3,0,0,0",
		}, 4},
		{[]string{
			"1,-1,0,1",
			"2,0,-1,0",
			"3,2,-1,0",
			"0,0,3,1",
			"0,0,-1,-1",
			"2,3,-2,0",
			"-2,2,0,0",
			"2,-2,0,-1",
			"1,-1,0,-1",
			"3,2,0,2",
		}, 3},
		{[]string{
			"1,-1,-1,-2",
			"-2,-2,0,1",
			"0,2,1,3",
			"-2,3,-2,1",
			"0,2,3,-2",
			"-1,-1,1,-2",
			"0,-2,-1,0",
			"-2,2,3,-1",
			"1,2,2,0",
			"-1,-2,0,-2",
		}, 8},
	}

	for i, test := range tests {
		actual := HotChocolatePortal(test.in)
		if actual != test.out {
			t.Errorf("HotChocolatePortal(#%d) => %d, want %d", i, actual, test.out)
		}
	}
}
